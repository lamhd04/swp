package entity;

import java.sql.Date;

/**
 *
 * @author hungl
 */
public class EmailExpire {
    private int id;
    private Date dueDate;

    public EmailExpire() {
    }

    public EmailExpire(int id, Date dueDate) {
        this.id = id;
        this.dueDate = dueDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDueDate() {
        return dueDate;
    }

    public void setDueDate(Date dueDate) {
        this.dueDate = dueDate;
    }
    
}
